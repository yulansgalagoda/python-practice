# ----Variables----
name = "yulan"

# ----Methods----
# length
print(len(name))

# index of a letter
print(name.find("l"))

# capitalise
print(name.capitalize())

# uppercase
print(name.upper())

# lower
print(name.lower())

# is it a number
print(name.isdigit())

# is it a set of  alphabetical characters
print(name.isalpha())

# number of characters
print(name.count("0"))

# replace characters
print(name.replace("a", "e"))

# print multiple times
print(name*3)
